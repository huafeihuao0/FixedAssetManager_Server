/**
 * 用户模型
 */
function UserModel()
{
    var model = {};

    function getUserId()
    {
        return typeof(model.userId) == "undefined" ? "" : model.userId;
    }

    function getUserName()
    {
        return typeof(model.userName) == "undefined" ? "" : model.userName;
    }

    function getSex()
    {
        return typeof(model.sex) == "undefined" ? "" : model.sex;
    }

    function getDeptId()
    {
        return typeof(model.deptId) == "undefined" ? "" : model.deptId;
    }

    function setUserId(userId)
    {
        this.model["userId"] = userId;
    }

    function setUserName(userName)
    {
        this.model["userName"] = userName;
    }

    function setSex(sex)
    {
        this.model["sex"] = sex;
    }

    function setDeptId(deptId)
    {
        this.model["deptId"] = deptId;
    }

    var ret=//
        {
            getUserId: getUserId,
            getUserName: getUserName,
            getSex: getSex,
            getDeptId: getDeptId,
            setUserId: setUserId,
            setUserName: setUserName,
            setSex: setSex,
            setDeptId: setDeptId
        };
    return ret;

}


exports.UserModel = UserModel;
